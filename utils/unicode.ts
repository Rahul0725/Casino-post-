export const toBold = (text: string): string => {
  return text.split('').map(char => boldMap[char] || char).join('');
};

export const toDoubleBold = (text: string): string => {
  return text.split('').map(char => doubleBoldMap[char] || char).join('');
};

export const styleText = (text: string, style: 'bold' | 'double' | 'normal' = 'bold'): string => {
  if (style === 'bold') return toBold(text);
  if (style === 'double') return toDoubleBold(text);
  return text;
};
